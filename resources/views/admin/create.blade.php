<script>
// Preview thumbnail
document.getElementById('thumbnail').addEventListener('change', function(e) {
    const file = e.target.files[0];
    if (file) {
        const reader = new FileReader();
        reader.onload = function(e) {
            const preview = document.createElement('img');
            preview.src = e.target.result;
            preview.className = 'mt-2 h-32 w-32 object-cover rounded-md';
            
            // Remove existing preview
            const existingPreview = document.querySelector('.thumbnail-preview');
            if (existingPreview) {
                existingPreview.remove();
            }
            
            // Add new preview
            preview.className += ' thumbnail-preview';
            document.querySelector('label[for="thumbnail"]').parentNode.parentNode.appendChild(preview);
        };
        reader.readAsDataURL(file);
    }
});
</script>
